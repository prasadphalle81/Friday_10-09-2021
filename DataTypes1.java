package Prasad_Java;

public class DataTypes1 {

	public static void main(String[] args) {
		
		boolean a1 = true;
		System.out.println("boolean a1 = " + a1);
		boolean a2 = false;
		System.out.println("boolean a2 = " + a2);
		
		byte b1 = -128;
		System.out.println("byte b1 = " + b1);
		byte b2 = (byte) 269;
		System.out.println("byte b2 = " + b2);
		
		short s1 = -32768;
		System.out.println("short s1 = " + s1);
		short s2 = (short)32965;
		System.out.println("short s2 = " + s2);
		
		int i1 = -32768;
		System.out.println("int i1 = " + i1);
		int i2 = (int)3890000;
		System.out.println("int i2 = " + i2);
		
		long lg1 = 100000;
		System.out.println("long lg1 = " + lg1);
		long lg2 = -2000047L;
		System.out.println("long lg1 = " + lg2);
	}

}
